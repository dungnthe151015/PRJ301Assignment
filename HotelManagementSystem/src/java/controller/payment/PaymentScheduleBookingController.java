    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int paymentID = Integer.parseInt(request.getParameter("paymentID"));
        Payment payment = new PaymentDBContext().getOne(paymentID);

        ArrayList<Order> list_order = new ArrayList<>();
        list_order = new OrderDBContext().getListByPayment(payment);

        request.setAttribute("payment", payment);
        request.setAttribute("list_order", list_order);
        request.getRequestDispatcher("../view/payment/schedule_booking.jsp").forward(request, response);
    }


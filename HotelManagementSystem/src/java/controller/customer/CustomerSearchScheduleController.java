        String raw_date = request.getParameter("search");

        Account account = (Account) request.getSession().getAttribute("account");
        Customer customer = new Customer();
        customer = new CustomerDBContext().getOneByAccountID(account);

        if (raw_date.equals("")) {
            response.sendRedirect("../customer/schedule_booking");

        } else {
            Date search = Date.valueOf(request.getParameter("search"));

            ArrayList<Payment> list_payment = new ArrayList<>();
            list_payment = new PaymentDBContext().getListBySearch(search);
            if (list_payment.size() == 0) {
                response.sendRedirect("../customer/no_payment");
            } else {
                request.setAttribute("list_payment", list_payment);
                request.getRequestDispatcher("../view/customer/customer_schedule_booking.jsp").forward(request, response);
            }
        }

